const fs = require("node:fs");
const path = require("node:path");

const sourceRoot = path.resolve(__dirname, "../src");
const outputRoot = path.resolve(__dirname, "../outputs/compensation-dashboard/src");
const files = [
  "format.js",
  "state.js",
  "model.js",
  "dom.js",
  "components/controls.js",
  "components/charts.js",
  "components/tables.js",
  "export.js",
  "main.js",
];

function stripImports(source) {
  return source.replace(/import\s+[\s\S]*?\s+from\s+["'][^"']+["'];\s*/g, "");
}

function stripExports(source) {
  return source
    .replace(/export\s+const\s+/g, "const ")
    .replace(/export\s+function\s+/g, "function ");
}

const body = files
  .map((file) => {
    const source = fs.readFileSync(path.join(sourceRoot, file), "utf8");
    return `\n// ${file}\n${stripExports(stripImports(source)).trim()}\n`;
  })
  .join("\n");

const bundle = `// Generated by tools/build-standalone.cjs from src ES modules.\n// Do not edit by hand; edit the modules and rerun the builder.\n(() => {\n\"use strict\";\n${body}\n})();\n`;

fs.mkdirSync(outputRoot, { recursive: true });
fs.writeFileSync(path.join(outputRoot, "standalone.js"), bundle);
